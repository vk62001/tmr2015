                <p class="negro">La Federación Mexicana de Robótica, A.C., y la Facultad de Estudios Superiores Acatlán de la Universidad Nacional Autónoma de México invitan a los roboticistas de todas las edades a participar en el Gran Torneo Mexicano de Robótica 2015, que se llevará a cabo en las instalaciones de la universidad en Naucalpan, Estado de México, del 23 al 25 de Abril del 2015. El Torneo Mexicano de Robótica (TMR) tiene un largo linaje de más de 20 años. Desde 1990, instituciones y profesores que hoy forman parte de la Federación Mexicana de Robótica (FMR) iniciaron en nuestro país la organización de competencias robóticas amistosas a través del Concurso Metropolitano de Robótica, el Concurso Mexicano de Robótica, y el Torneo Mexicano de Robots Limpiadores.</p>

<p class="negro">Los objetivos del TMR son:</p>
