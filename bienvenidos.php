                <h2 class="titulo3" >Bienvenidos</h2>
                <p class="p">La Federación Mexicana de Robótica, A.C., y la Facultad de Estudios Superiores Acatlán de la Universidad Nacional Autónoma de México invitan a los roboticistas de todas las edades a participar en el Gran Torneo Mexicano de Robótica 2015, que se llevará a cabo en las instalaciones de la universidad en Naucalpan, Estado de México, del 23 al 25 de Abril del 2015. El Torneo Mexicano de Robótica (TMR) tiene un largo linaje de más de 20 años. Desde 1990, instituciones y profesores que hoy forman parte de la Federación Mexicana de Robótica (FMR) iniciaron en nuestro país la organización de competencias robóticas amistosas a través del Concurso Metropolitano de Robótica, el Concurso Mexicano de Robótica, y el Torneo Mexicano de Robots Limpiadores.</p>

<p class="p">Los objetivos del TMR son:</p>
                <ul>
                    <p class="p">Promover, a través de la organización de competencias regionales y nacionales, el desarrollo de tecnologías inteligentes relacionadas con la robótica y la automatización.</p>
                    <p class="p">Impulsar el desarrollo de proyectos de robótica educativa que estimulen el gusto por la ciencia y la tecnología, y que redunden en formación de científicos, ingenieros, y técnicos especializados.</p>
                    <p class="p">Impulsar el desarrollo de robots autónomos capaces de resolver problemas de interés para la sociedad.
Difundir los avances de los roboticistas mexicanos en la sociedad.</p>
                </ul>
            <p class="p">El TMR es una fiesta de la robótica que reúne cada año alrededor de 150 equipos que suman más de 700 participantes provenientes de todo el país. ¡No faltes!</p>
